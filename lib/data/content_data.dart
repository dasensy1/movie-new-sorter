  static List<ContentItem> getMovies() {
    return allContent.where((item) => item.type == 'movie').toList();
  }

  static List<ContentItem> getSeries() {
    return allContent.where((item) => item.type == 'series').toList();
  }

  static List<ContentItem> getByGenre(String genre) {
    return allContent.where((item) => item.genre == genre).toList();
  }

  static List<ContentItem> search(String query) {
    final lowerQuery = query.toLowerCase();
    return allContent
        .where((item) =>
            item.title.toLowerCase().contains(lowerQuery) ||
            item.director.toLowerCase().contains(lowerQuery) ||
            item.genre.toLowerCase().contains(lowerQuery))
        .toList();
  }

  static List<String> getGenres() {
    final genres = allContent.map((item) => item.genre).toSet().toList();
    genres.sort();
    return genres;
  }
